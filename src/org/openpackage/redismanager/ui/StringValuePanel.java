    private void initValueTypes() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement(RedisKey.VALUE_STRING);
        model.addElement(RedisKey.VALUE_JSON);
        valueTypeComboBox.setModel(model);
    }
    
