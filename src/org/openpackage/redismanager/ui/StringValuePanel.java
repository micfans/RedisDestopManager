    void reloadValue() {
        currentRedisKey.loadValue();
        nameTextField.setText(currentRedisKey.getName());
        valueTextPane.setText(currentRedisKey.getValue());
        ttlLabel.setText(currentRedisKey.getTTL() + "");
    }
    
